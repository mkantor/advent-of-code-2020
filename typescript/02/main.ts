type PasswordPolicy = {
    constraints: [number, number]
    letter: Letter
}
type Password = string
type Passwords = [PasswordPolicy, Password][]

function isPasswordValidForSledShop(
    policy: PasswordPolicy,
    password: Password,
): boolean {
    let policyLetterCount = 0
    for (let character of password) {
        if (character === policy.letter) {
            policyLetterCount++
        }
    }
    return (
        policyLetterCount >= policy.constraints[0] &&
        policyLetterCount <= policy.constraints[1]
    )
}

function isPasswordValidForTobogganShop(
    policy: PasswordPolicy,
    password: Password,
): boolean {
    const characterAtFirstPosition = password[policy.constraints[0] - 1]
    const characterAtSecondPosition = password[policy.constraints[1] - 1]
    return (
        (characterAtFirstPosition === policy.letter) !==
        (characterAtSecondPosition === policy.letter)
    )
}

function countWithPredicate<T>(
    inputs: T[],
    predicate: (input: T) => boolean,
): number {
    return inputs.reduce((count, input) => {
        return predicate(input) ? count + 1 : count
    }, 0)
}

